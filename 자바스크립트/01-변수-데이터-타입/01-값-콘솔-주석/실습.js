1.21;
// 소수
0.12;
// 정수
2;
// 음수
-3;
// 하나 ❌
// two ❌

// -----------------------------
// 문자(strung) 값
// 작은따옴표 (')
("2px");
// 2px -> 그대로 쓰면 문법오류 발생
// 큰따옴표(")
("2mm");
// -> 오류발생 ";"가 없기때문
// 프로그램 문장이 끝날 때 마침을 의미하는 세미콜론추가
// 역따옴표(`)
`2inch`;

